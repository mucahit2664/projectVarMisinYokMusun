 package oyun;

public class Kutu {

	 private int kutuNumarasi, paraMiktari;
	    
	    public Kutu(int sayi1, int sayi2) {   // sayi1 - kutuNumarasi       sayi2 - paraMiktari
	        this.kutuNumarasi = sayi1;
	        this.paraMiktari  = sayi2;
	    }
	    public int getKutuNumarasi() {
	        return kutuNumarasi;
	    }
	    public int getParaMiktari() {
	        return paraMiktari;
	    }
	    
	}
